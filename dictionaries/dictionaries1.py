# Provides with a means of storing data in a more meaningful way
# Sometimes we need a more structured way of storing our information in a collection. To do this, we use dictionaries


# Dictionaries allow us to take things a step further when it comes to storing information in a collection. Dictionaries
# will enable us to use what are called key/value pairs. When using a dictionary, we define our key/value pairs enclosed 
# in curly braces ({}). After that, we use a string as our key. Then we use a colon to separate the key from the value, 
# and then we have the value.


user = {
    "username": "tombombadil",
    "first_name": "Tom",
    "last_name": "Bombadil",
    "age": 100
}

print(user)

# OUTPUT:
# {'username': 'tombombadil', 'first_name': 'Tom', 'last_name': 'Bombadil', 'age': 100}

# NOTES:
# In this case, we have a dictionary called user. This dictionary has four keys (username, first_name, last_name, and age). 
# Each of these keys has a value associated with it (“tombombadil”, “Tom”, “Bombadil”, 100).    


