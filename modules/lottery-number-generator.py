#Let’s use what we’ve learned so far to create a lottery number generator. Before we can build it, we need to determine precisely 
#how it will work. We have decided that we want to generate ten numbers, each ranging from 1 to 50. Let’s create a function that 
#can do that.

from random import randint


def lottery_generator():
    numbers = []
    for number in range(0, 10):
        numbers.append(randint(1, 50))
    return numbers

print("This weeks winning lottery numbers are %s" % lottery_generator())

