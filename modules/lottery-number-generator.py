# Here we import the randint function. Then we define a new function called lottery_generator. Inside the lottery_generator 
# function, we create an empty list called numbers. After that, we create a for loop a with a range of 
# 0 to 10. For each iteration of the loop, we create a new item in the numbers list by using the append 
# method. The append method will append a new item to the end of the list, and that new item is a randomly 
# generated int from a range between 1 and 50. Once we’ve iterated over the sequence generated by range, 
# we just return the list. Then we print out a message that tells the user what the numbers were for 
