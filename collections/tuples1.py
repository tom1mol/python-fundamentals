# Provide us with another means of storing collections of data
# Sometimes a list doesn’t accurately fulfil our needs; in which case, we would use tuples instead
# In addition to lists, we can also store information in tuples. We’ve seen these before when working
# with the print when we were formatting strings. Here’s a reminder!

name = input("What's your name?")
age = input("What age are you?")

print("%s is %s years old" % (name, age))

# OUTPUT:
# What's your name? JO
# What age are you? 6
# JO is 6 years old

# NOTES:
# A tuple is denoted by the parentheses (). And the information contained within are the values of that tuple.
# NOTE: Tuples are not to be confused with function parameters or arguments.

  
